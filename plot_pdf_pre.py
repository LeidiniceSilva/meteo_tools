# -------------------------------------------------------------------
# Flatten data
# -------------------------------------------------------------------
precip = []
for year in data:
    for v in data[year]:
        if v is not None:
            precip.append(v)

precip = np.array(precip)

# -------------------------------------------------------------------
# KDE
# -------------------------------------------------------------------
kde = gaussian_kde(precip)
x = np.linspace(0, precip.max() + 20, 500)
pdf = kde(x)

# -------------------------------------------------------------------
# Percentiles
# -------------------------------------------------------------------
p5, p25, p50, p75, p95, p99 = np.percentile(
    precip, [5, 25, 50, 75, 95, 99]
)

# -------------------------------------------------------------------
# Plot
# -------------------------------------------------------------------
plt.figure(figsize=(9,5))

# background light gray
ax = plt.gca()
